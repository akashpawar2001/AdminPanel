package Model;

public class Feedback {
	private String feedbackerName;
	private String feedbackerMessage;
	public String getFeedbackerName() {
		return feedbackerName;
	}
	public void setFeedbackerName(String feedbackerName) {
		this.feedbackerName = feedbackerName;
	}
	public String getFeedbackerMessage() {
		return feedbackerMessage;
	}
	public void setFeedbackerMessage(String feedbackerMessage) {
		this.feedbackerMessage = feedbackerMessage;
	}
}
